// Converts a YAML config file into an env file ("dot-env" format, written
// WITHOUT a leading dot — e.g. pryv-config.env) so a deployment can run
// with no config file at all: the server reads every key from environment
// variables. Pairs with the diskless deployment shape.
//
// Key mapping: nested YAML paths join with '__' (double underscore), the
// separator the server's config loader uses for environment variables.
// Values survive round-trip typing: scalars are emitted bare; strings
// that would otherwise re-parse as a number/boolean (e.g. a serial like
// '20260611') are emitted JSON-quoted; arrays/objects used as leaf values
// are emitted as JSON.
//
// The output targets `docker run --env-file` (raw KEY=VALUE lines, no
// shell quoting). For `source`-style shell use, quote values yourself.
//
// Usage:
//   docker run --rm -v /host/config:/app/pryv \
//     pryvio/open-pryv.io config-to-env /app/pryv/pryv-config.yml
//
// Locally:
//   node bin/config-to-env.js <config.yml> [output.env]
//
// Then boot without any config mount:
//   docker run --env-file pryv-config.env pryvio/open-pryv.io
//
// Exit 0 = written. Exit 1 = error (printed).

'use strict';

const fs = require('fs');
const path = require('path');

let yaml;
try {
  yaml = require('js-yaml');
} catch (err) {
  console.error('config-to-env: js-yaml is required but not installed.');
  process.exit(1);
}

const configPath = process.argv[2];
if (!configPath) {
  console.error('Usage: config-to-env <config-path> [output-path]');
  process.exit(1);
}
const absPath = path.resolve(configPath);
if (!fs.existsSync(absPath)) {
  console.error(`config-to-env: file not found: ${absPath}`);
  process.exit(1);
}

let config;
try {
  config = yaml.load(fs.readFileSync(absPath, 'utf8'));
} catch (e) {
  console.error(`config-to-env: failed to parse YAML: ${e.message}`);
  process.exit(1);
}
if (config == null || typeof config !== 'object' || Array.isArray(config)) {
  console.error('config-to-env: config root must be a YAML mapping.');
  process.exit(1);
}

const outPath = process.argv[3]
  ? path.resolve(process.argv[3])
  : absPath.replace(/\.ya?ml$/i, '') + '.env';

const lines = [];
const skipped = [];
flatten(config, []);

function flatten (value, keyPath) {
  if (value != null && typeof value === 'object' && !Array.isArray(value)) {
    const keys = Object.keys(value);
    if (keys.length === 0) {
      emit(keyPath, encodeValue(value));
      return;
    }
    for (const key of keys) flatten(value[key], keyPath.concat(key));
    return;
  }
  emit(keyPath, encodeValue(value));
}

// The server's env loader runs each value through JSON-parse TWICE (once
// in the env reader, once in the underlying store), falling back to the
// raw string at each step. Encode so the value that comes out matches the
// YAML exactly:
// - plain strings that don't parse as JSON → raw (both parses no-op)
// - numbers / booleans / null → single JSON (parse 1 restores the value,
//   parse 2 round-trips it unchanged)
// - arrays / objects → DOUBLE JSON (parse 1 yields the JSON text, parse 2
//   the value — single encoding would let parse 2 mangle e.g. [1] via
//   string coercion)
// - strings that WOULD parse as something else ('20260611', 'true') →
//   DOUBLE JSON so they come out as strings, not numbers/booleans
function encodeValue (value) {
  if (typeof value === 'string') {
    try {
      JSON.parse(value);
    } catch (e) {
      return value; // not JSON — raw survives both parses
    }
    return JSON.stringify(JSON.stringify(value));
  }
  if (value != null && typeof value === 'object') {
    return JSON.stringify(JSON.stringify(value));
  }
  return JSON.stringify(value); // number | boolean | null
}

function emit (keyPath, encoded) {
  const key = keyPath.join('__');
  if (/[\n\r]/.test(encoded)) {
    skipped.push(key);
    return;
  }
  lines.push(`${key}=${encoded}`);
}

const header = [
  `# Generated by config-to-env from ${path.basename(absPath)} on ${new Date().toISOString().slice(0, 10)}.`,
  '# One environment variable per config key; nested paths join with __.',
  '# Use with: docker run --env-file <this file> pryvio/open-pryv.io',
  '# (raw KEY=VALUE lines — for shell `source` use, add quoting yourself)',
  ''
].join('\n');

fs.writeFileSync(outPath, header + lines.join('\n') + '\n');
console.log(`✓ Wrote ${outPath} (${lines.length} variable(s))`);
if (skipped.length > 0) {
  console.error(`  ⚠ Skipped ${skipped.length} key(s) with multi-line values (env files are line-based):`);
  skipped.forEach((k) => console.error(`    - ${k}`));
  console.error('  Set these through another channel (config file or orchestrator secrets).');
}
process.exit(0);
